numbers = [10, 501, 22, 37, 100, 997, 87, 351]           # Created a list called numbers.
even_numbers = [num for num in numbers if num %2 == 0]   # To find the even numbers.
print("Even_numbers:",  even_numbers)                    # Printing the even numbers.
odd_numbers = [num for num in numbers if num %2 !=0]     # Used for loop to find the odd numbers.
print("Odd_numbers:", odd_numbers)                       # Ton display the odd numbers.
